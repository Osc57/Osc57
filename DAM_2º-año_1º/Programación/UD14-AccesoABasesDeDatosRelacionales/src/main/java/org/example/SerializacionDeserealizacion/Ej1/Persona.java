package org.example.SerializacionDeserealizacion.Ej1;

public class Persona {
    private String nombre;
    private int edad;
    private String correo;

    public Persona() {
    }

    public Persona(String nombre, int edad, String correo) {
        this.nombre = nombre;
        this.edad = edad;
        this.correo = correo;
    }
}
