
//1. Realiza un programa que calcule la longitud de una circunferencia de radio 3 metros.

public class Ej1 {
    public static void main(String[] args) {

        double radio = 3;
        double longitud;

        longitud = 2 * Math.PI * radio;

        System.out.println("La longitud del radio es: " + longitud);
    }
}
